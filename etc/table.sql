CREATE TABLE board (
    idx INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
    title VARCHAR(50) NOT NULL,
    content TEXT NULL,
    nickname VARCHAR(30) NOT NULL,
    date TIMESTAMP NOT NULL,
    view INT NOT NULL DEFAULT 0,
    likes INT NOT NULL DEFAULT 0,
    userid VARCHAR(30) NOT NULL
);

CREATE TABLE comment (
    cid INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
    comment TEXT NOT NULL,
    c_nickname VARCHAR(30) NOT NULL,
    c_date TIMESTAMP NOT NULL,
    bid INT NOT NULL,
    c_userid VARCHAR(30) NOT NULL
);

CREATE TABLE user (
    userid VARCHAR(30) NOT NULL PRIMARY KEY,
    userpw VARCHAR(50) NOT NULL,
    username VARCHAR(30) NOT NULL,
    nickname VARCHAR(30) NOT NULL,
    birth VARCHAR(20) NOT NULL,
    gender VARCHAR(20) NOT NULL,
    phone VARCHAR(30) NULL,
    mobile VARCHAR(30) NOT NULL,
    level INT NOT NULL DEFAULT 3,
    access VARCHAR(20) NOT NULL DEFAULT 'true'
);

CREATE TABLE scrap (
    s_idx INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
    s_userid VARCHAR(30) NOT NULL,
    bid INT NOT NULL
);

CREATE TABLE likes (
    likes_idx INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
    likes_userid VARCHAR(30) NOT NULL,
    bid INT NOT NULL
);